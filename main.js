document.getElementById('showL').addEventListener('click', function(){
    document.getElementById('u2').style.display = 'block';
    document.getElementById('showL').style.display = 'none';
})

document.getElementById('hideL').addEventListener('click', function(){
    document.getElementById('u2').style.display = 'none';
    document.getElementById('showL').style.display = 'block';
})